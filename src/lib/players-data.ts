export function findPlayerByName(name: string): RegisteredPlayer | undefined {
  return ALL_PLAYERS.find(p => p.Person.toLowerCase() === name.toLowerCase());
}

export function findPlayerById(id: number): RegisteredPlayer | undefined {
  return ALL_PLAYERS.find(p => p.PersonId === id);
}

export function getTeammates(team: string | null): RegisteredPlayer[] {
  if (!team) return [];
  return ALL_PLAYERS.filter(p => p.Team === team);
}

export function getCategoryCount(): Record<string, number> {
  const counts: Record<string, number> = {};
  for (const p of ALL_PLAYERS) {
    const cat = p.Category || "Necategorizat";
    counts[cat] = (counts[cat] || 0) + 1;
  }
  return counts;
}
